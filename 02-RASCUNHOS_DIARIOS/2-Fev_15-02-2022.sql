JNT.NIVEL_INSPECAO,
ISI.TIPO_JUNTA,
jnt.extensao,
decode(jnt.execucao
, 0, 'OFC'
, 1, 'MNT'
, 2, 'PMN'
, 3, 'EDF') as stage,
