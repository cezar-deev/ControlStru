isi.US_data

        from vw_isireport_juntainspecionada isi
        inner join jntjunta jnt on jnt.cod_junta = isi.cod_junta
        left join attdi att on jnt.bold_id = att.pjntjunta

             where jnt.cod_junta like '%SMP-063/B1-02%'
             order by 1
